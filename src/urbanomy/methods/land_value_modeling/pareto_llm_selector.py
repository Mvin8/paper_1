"""LangGraph-based multi-agent voting over Pareto-front scenarios.

The module provides a notebook-friendly API for selecting one scenario from a
Pareto frontier using three stakeholder agents:

* administration
* residents
* investor

Each stakeholder is implemented as a LangGraph ReAct agent with deterministic
tools over the frontier. Agents return strict JSON that is validated with
Pydantic and ``JsonOutputParser``. A graph-level aggregator then computes the
final winner by majority vote with Borda and confidence tie-breakers.
"""

