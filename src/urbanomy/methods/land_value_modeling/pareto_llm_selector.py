def _format_money(value: Any) -> str:
    """Format money-like values for autogenerated summaries."""
    try:
        numeric = float(value)
    except (TypeError, ValueError):
        return "n/a"
    if math.isnan(numeric) or math.isinf(numeric):
        return "n/a"
    return f"{numeric:,.0f} ₽".replace(",", " ")


def _scenario_summary_from_row(
    *,
    scenario_id: str,
    title: str,
    land_use: Any = None,
    land_value_gain: Any = None,
    investor_npv: Any = None,
) -> str:
    """Build a compact text summary when an explicit summary column is absent."""
    parts = [title or f"Сценарий {scenario_id}"]
    if land_use not in (None, "", "nan"):
        parts.append(f"land_use={land_use}")
    if land_value_gain is not None:
        parts.append(f"прирост стоимости земли={_format_money(land_value_gain)}")
    if investor_npv is not None:
        parts.append(f"NPV инвестора={_format_money(investor_npv)}")
    return "; ".join(parts)


class ParetoScenario(BaseModel):
    """Canonical representation of one Pareto-front scenario."""

    model_config = ConfigDict(extra="forbid")

    scenario_id: str = Field(description="Unique scenario identifier.")
    title: str = Field(default="", description="Short human-readable title.")
    summary: str = Field(default="", description="One-paragraph scenario summary.")
    metrics: dict[str, float | int | bool | str | None] = Field(
        default_factory=dict,
        description="Scenario KPIs used during decision-making.",
    )
    pros: list[str] = Field(default_factory=list)
    cons: list[str] = Field(default_factory=list)
    metadata: dict[str, Any] = Field(default_factory=dict)

    @field_validator("scenario_id", mode="before")
    @classmethod
    def _coerce_scenario_id(cls, value: Any) -> str:
        text = str(value).strip()
        if not text:
            raise ValueError("scenario_id cannot be empty")
        return text

    @field_validator("title", "summary", mode="before")
    @classmethod
    def _coerce_text(cls, value: Any) -> str:
        if value is None:
            return ""
        return str(value).strip()


class StakeholderVote(BaseModel):
    """Structured JSON returned by each stakeholder agent."""

    model_config = ConfigDict(extra="forbid")

    stakeholder: StakeholderName
    selected_scenario_id: str = Field(description="Scenario chosen by the stakeholder.")
    ranking: list[str] = Field(
        default_factory=list,
        description="Scenarios ordered from best to worst from this stakeholder's perspective.",
    )
    rationale: str = Field(description="Short explanation of the vote.")
    main_tradeoffs: list[str] = Field(default_factory=list)
    metrics_considered: list[str] = Field(default_factory=list)
    confidence: float = Field(ge=0.0, le=1.0, description="Confidence in the vote from 0 to 1.")
    used_tools: list[str] = Field(default_factory=list)

    @model_validator(mode="after")
    def _ensure_selected_in_ranking(self) -> "StakeholderVote":
        ranking = [str(item).strip() for item in self.ranking if str(item).strip()]
        if not ranking:
            ranking = [self.selected_scenario_id]
        if self.selected_scenario_id not in ranking:
            ranking.insert(0, self.selected_scenario_id)
        if ranking[0] != self.selected_scenario_id:
            ranking = [self.selected_scenario_id, *[item for item in ranking if item != self.selected_scenario_id]]
        self.ranking = ranking
        return self


